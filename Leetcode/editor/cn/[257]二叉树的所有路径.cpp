/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class Solution {
private:
    vector<string> res;

public:
    void dfs(TreeNode *root, string path) {
        path += to_string(root->val);
        if (!root->left && !root->right)
            res.push_back(path);
        if (root->left)
            dfs(root->left, path + "->");
        if (root->right)
            dfs(root->right, path + "->");
    }
    vector<string> binaryTreePaths(TreeNode *root) {
        if (!root)
            return {};
        dfs(root, "");
        return res;
    }
};
