/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
class Solution {
public:
    ListNode *addTwoNumbers(ListNode *l1, ListNode *l2) {
        if (!l1 || !l2) {
            return l1 == nullptr ? l2 : l1;
        }
        int carried = 0;//è¿›ä½
        int a, b, sum;
        stack<int> s1, s2;
        ListNode *res = nullptr;//åˆ›å»ºå¤´èŠ‚ç‚¹
        while (l1) {
            s1.push(l1->val);
            l1 = l1->next;
        }
        while (l2) {
            s2.push(l2->val);
            l2 = l2->next;
        }
        while (!s1.empty() || !s2.empty() || carried) {
            a = s1.empty() ? 0 : s1.top();
            b = s2.empty() ? 0 : s2.top();
            if (!s1.empty())
                s1.pop();
            if (!s2.empty())
                s2.pop();
            sum = a + b + carried;
            carried = sum / 10;
            sum %= 10;
            ListNode *cur = new ListNode(sum);
            cur->next = res;
            res = cur;
        }
        return res;
    }
};
