/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class Solution {
public:
    vector<double> res;

    void bfs(TreeNode *root) {
        queue<TreeNode *> q;
        q.push(root);
        while (!q.empty()) {
            double sum = 0;
            int n = q.size();
            for (int i = 0; i < n; i++) {
                TreeNode *temp = q.front();
                q.pop();
                if (temp->left)
                    q.push(temp->left);
                if (temp->right)
                    q.push(temp->right);
                sum += temp->val;
            }
            res.push_back(sum / n);
        }
    }

    vector<double> averageOfLevels(TreeNode *root) {
        bfs(root);
        return res;
    }
};
